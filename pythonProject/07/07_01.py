print("Числа заданы в разных системах:")
A=0b10011 #19 в двоичной
print("A =", A)
B=0o135 #93 в восьмеричной
print("B =" , B)
C=0xA2B #2603 в шестнадцатиричной
print("C =", C)
print("Обратное преобразование:")
print(bin(A), "=", A)
print(oct(B), "=", B)
print(hex(C), "=", C)
print("Перевод числа 25 в разные системы:")
D = 25
print("D(25) в десятичной:", D)
print("D(25) в двоичной:", bin(D))
print("D(25) в восьмеричной:", oct(D))
print("D(25) в 16-ричной:", hex(D))